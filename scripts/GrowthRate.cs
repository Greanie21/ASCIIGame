public class GrowthRate
{
    public float healthPoints;
    public float attack;
    public float magicAttack;
    public float defense;
    public float magicDefense;
    public float speed;
    public float accuracy;
    public float dodge;

    public GrowthRate()
    {
        healthPoints = 0;
        attack = 0;
        magicAttack = 0;
        defense = 0;
        magicDefense = 0;
        speed = 0;
        accuracy = 0;
        dodge = 0;
    }
}

